  List<MHIdea> getAllIdeas() => List.unmodifiable(_ideas);

  MHIdea? getIdeaById(String id) {
    try {
      return _ideas.firstWhere((idea) => idea.id == id);
    } catch (_) {
      return null;
    }
  }

  List<MHIdea> getIdeasByCategory(MHIdeaCategory category) {
    return _ideas.where((idea) => idea.category == category).toList();
  }

  List<MHIdea> getIdeasByInvestment(MHInvestmentType investment) {
    return _ideas.where((idea) => idea.investmentType == investment).toList();
  }

  List<MHIdea> searchIdeas(String query) {
    final lowerQuery = query.toLowerCase();
    return _ideas.where((idea) {
      return idea.title.toLowerCase().contains(lowerQuery) ||
          idea.preview.toLowerCase().contains(lowerQuery) ||
          idea.category.label.toLowerCase().contains(lowerQuery);
    }).toList();
  }

  List<MHIdea> filterIdeas({
    String? query,
    Set<MHIdeaCategory>? categories,
    Set<MHInvestmentType>? investments,
  }) {
    return _ideas.where((idea) {
      if (query != null && query.isNotEmpty) {
        final lowerQuery = query.toLowerCase();
        if (!idea.title.toLowerCase().contains(lowerQuery) &&
            !idea.preview.toLowerCase().contains(lowerQuery)) {
          return false;
        }
      }
      if (categories != null && categories.isNotEmpty) {
        if (!categories.contains(idea.category)) return false;
      }
      if (investments != null && investments.isNotEmpty) {
        if (!investments.contains(idea.investmentType)) return false;
      }
      return true;
    }).toList();
  }

  List<MHIdea> getDailyIdeas({int limit = 5}) {
    final now = DateTime.now();
    final seed = now.year * 1000 + now.month * 100 + now.day;
    final shuffled = List<MHIdea>.from(_ideas);
    _shuffleWithSeed(shuffled, seed);
    return shuffled.take(limit).toList();
  }
}

void _shuffleWithSeed<T>(List<T> list, int seed) {
  var s = seed;
  for (var i = list.length - 1; i > 0; i--) {
    s = (s * 1103515245 + 12345) & 0x7fffffff;
    final j = s % (i + 1);
    final temp = list[i];
    list[i] = list[j];
    list[j] = temp;
  }
}
